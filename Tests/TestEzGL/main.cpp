#define IfTestExist(v)            \
    if (vTest == std::string(#v)) \
    return v()

#define IfTestCollectionExist(v, str)        \
    if (vTest.find(#v) != std::string::npos) \
    return v(str)

bool TestEzGL(const std::string& vTest) {
    IfTestCollectionExist(Test_EzGL_Uniforms, vTest);
    return true;
}

////////////////////////////////////////////////////////////////////////////
//// ENTRY POINT ///////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

int main(int argc, char** argv) {
    if (argc > 1) {
        printf("Exec test : %s\n", argv[1]);
        return TestEzGL(argv[1]) ? 0 : 1;
    }
    // User testing
    return TestEzGL("Test_EzGL_Uniforms_Parsing_Good_Syntax_10") ? 0 : 1;
}
